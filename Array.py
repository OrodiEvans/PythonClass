# Used to display list
courses = ["MIT", "CYBERSECURITY", "DATASCIENCE"]
print(courses)

# Accessing elements
print(courses[1])

# Looping through array elements
for mycourse in courses:
    print(mycourse)

# Adding an element in an array
courses.append("machine learning")
print(courses)

# How to remove/delete an element
courses.remove("machine learning")
print(courses)
