# Arithmetic Operators
x = 40
y = 12
print(x + y)
print(x * y)
print(x - y)
print(x / y)
print(x % y)  # Modulus

# Comparison Operators
print(x < y)
print(y > x)
print(x >= y)
print(y <= x)
print(y >= x)
print(x == y)

# Assignment Operators
a = 20
print(a)
b = 12
b += 2
print(b)

c = 12
c -= 2
print(c)

# Logical Operators
num1 = 34
num2 = 67
print(num1 > num2 and num2 > num1)
print(num1 < num2 and num2 > num1)
print(num1 > num2 or num2 < num1)
print(num1 > num2 or num2 > num1)
print(not(num1 > num2 and num2 > num1))
print(not(num1 < num2 and num2 > num1))
print(not(num1 > num2 or num2 > num1))
print(not(num1 < num2 or num2 > num1))
